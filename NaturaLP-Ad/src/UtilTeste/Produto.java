package UtilTeste;

public class Produto {
	
	private String ProdNome;
	private String ProdTip;
	private double ProdVal;
	
	public Produto(String ProdNome, String ProdTip, double ProdVal){
		
	}

	public Produto(){
		
	}
	
	public String getProdNome() {
		return ProdNome;
	}

	public void setProdNome(String prodNome) {
		ProdNome = prodNome;
	}

	public String getProdTip() {
		return ProdTip;
	}

	public void setProdTip(String prodTip) {
		ProdTip = prodTip;
	}

	public double getProdVal() {
		return ProdVal;
	}

	public void setProdVal(double prodVal) {
		ProdVal = prodVal;
	}

	
	
	
}
